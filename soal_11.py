#step 1
m = int(input("berapa nilai m: "))
n = int(input("berapa nilai n: "))

#step 2
sisi_a = m**2 - n**2
sisi_b = 2*m*n 
sisi_c = m**2 + n**2

#step 3
print(f"hasil dari sisi a adalah: {sisi_a}. \nHasil Dari Sisi b adalah: {sisi_b}. \nHasil Dari Sisa c adalah: {sisi_c}.")